THRESHOLD = 0.34  # ниже — «неизвестно» (повод для vision-фолбэка)


def classify_table(columns: list, rows: list | None = None, title: str | None = None) -> dict:
    """Тип таблицы по шапке (+ опц. названию). Возвращает {type,label,about,route,
    confidence,matched,scores}. type='неизвестно' при слабом сигнале."""
    cols = [_norm(c) for c in (columns or [])]
    blob = " | ".join(cols)
    title_n = _norm(title) if title else ""

    scores: dict[str, float] = {}
    matched: dict[str, list] = {}
    for t, sig in SIGNATURES.items():
        hits = [kw for kw in sig["headers"] if kw in blob]
        score = len(hits) / max(len(sig["headers"]), 1)
        if title_n:
            tmatch = [tk for tk in sig.get("title", []) if tk in title_n]
            if tmatch:
                # название — сильнейший сигнал; длиннее совпадение = специфичнее
                # («кабельный журнал» бьёт «журнал», иначе generic выигрывает ничью)
                score += 1.5 + 0.03 * max(len(tk) for tk in tmatch)
        scores[t] = round(score, 3)
        matched[t] = hits

    best = max(scores, key=scores.get) if scores else None
    conf = scores.get(best, 0.0) if best else 0.0
    if not best or conf < THRESHOLD:
        return {"type": "неизвестно", "label": "тип не распознан", "about": "",
                "route": "", "confidence": round(conf, 2), "matched": [], "scores": scores}
    sig = SIGNATURES[best]
    return {"type": best, "label": sig["label"], "about": sig["about"], "route": sig["route"],
            "confidence": round(conf, 2), "matched": matched[best], "scores": scores}
