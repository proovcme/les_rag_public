"""Голос Совушки для детерминированных ответов: ирония поверх фактов (числа строгие).

Детерминированные каналы (glossary/smeta/help) идут МИМО LLM → ответы плоские («эксель»).
Этот слой возвращает характер (сыч, «изящное хамство») БЕЗ LLM: короткая реплика-рамка вокруг
факта. Выбор реплики стабилен по хэшу запроса (тестируемо, без рандома). Числа/факты не трогаются.
См. [[sovushka-tone-and-memory]].

И страховка от «техподдержки Сбера»: `wants_model()` — явное «хочу к модели» (своими словами,
поговорим, как считаешь) → канал должен уступить дорогу LLM/RAG. Плюс каналы и так возвращают None
на всё, что не явный lookup — открытый вопрос сам доходит до модели.
"""

from __future__ import annotations

# Короткие реплики-рамки по типу ответа. Сыч язвит, но по делу; числа идут следом нетронутыми.
_OPENERS: dict[str, tuple[str, ...]] = {
    "glossary": ("Извольте, ликбез.", "Раз спросили — объясняю, переспрашивать потом неприлично.",
                 "Записывайте, повторять не буду."),
    "price": ("Касса не врёт:", "Ценник, без сантиментов:", "Сколько просят:"),
    "needs_kac": ("Сверяюсь с базой, не с наитием:", "По регламенту, не по настроению:"),
    "stesnennost": ("Зануднейший из коэффициентов, но извольте:", "Стеснённость — это про деньги, держите:"),
    "assemble": ("Собрала. Считал код, не я на пальцах:", "Готово, цифры выверены — можешь не перепроверять:"),
    "help": ("Чем богаты. Прежде чем в третий раз спросить то же самое:", "Список. Изучайте, я подожду."),
}

# Явное «хочу к живой модели, не к справочнику» — канал уступает дорогу (анти-Сбер).
_BYPASS = (
    "своими словами", "просто ответь", "просто поговор", "поговорим", "по-человечески",
    "по человечески", "без шаблон", "без справочник", "не из справочник", "как считаешь",
    "что думаешь", "не по канал", "живьём", "живьем", "порассужда", "рассуди",
)


def wants_model(question: str) -> bool:
    """Явное намерение «к модели, не к справочнику» → детерм. канал должен вернуть None."""
    q = (question or "").lower()
    return any(p in q for p in _BYPASS)


def _pick(pool: tuple[str, ...], seed: str) -> str:
    if not pool:
        return ""
    return pool[sum(ord(c) for c in (seed or "x")) % len(pool)]


def flavor(answer: str, op: str, *, seed: str = "") -> str:
    """Короткая ироничная рамка перед фактом. Числа/факты не трогаются. Пустой op → как есть."""
    opener = _pick(_OPENERS.get(op, ()), seed or (answer[:24] if answer else "x"))
    return f"{opener}\n{answer}" if opener else answer
